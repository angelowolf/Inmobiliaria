package Persistencia.Modelo;

import java.util.Date;

/**
 *
 * @author Angelo
 */
public class Usuario {

    private int idUsuario;
    private String nick, clave, nombre, apellido, codigo, email;
    private Date fechaCreacionCodigo;

    public Usuario() {
    }

    public Date getFechaCreacionCodigo() {
        return fechaCreacionCodigo;
    }

    public void setFechaCreacionCodigo(Date fechaCreacionCodigo) {
        this.fechaCreacionCodigo = fechaCreacionCodigo;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    public String getApellido() {
        return apellido;
    }

    public void setApellido(String apellido) {
        this.apellido = apellido;
    }

    public String getCodigo() {
        return codigo;
    }

    public void setCodigo(String codigo) {
        this.codigo = codigo;
    }

    public int getIdUsuario() {
        return idUsuario;
    }

    public void setIdUsuario(int idUsuario) {
        this.idUsuario = idUsuario;
    }

    public String getNick() {
        return nick;
    }

    public void setNick(String nick) {
        this.nick = nick;
    }

    public String getClave() {
        return clave;
    }

    public void setClave(String clave) {
        this.clave = clave;
    }

}
